        <h2>PD Libraries</h2>
        <p>
            During the Amiga's peak either side of 1990, the Internet was rarely available to home users. Instead, other
            distribution channels for software appeared. There was obviously the commercial Amiga software which could
            be brought in high street stores and by mail order. But there was also the PD Library - typically small
            businesses or even individuals, who kept a "library" of disks that they would duplicate onto disk and send
            out mail order for a small charge. Some of these became very big, such as 17 Bit Software, who created their
            own disk library, and curated a community through their memberships and update disks. The other notable PD
            Library was the "Amiga Library Disk" series curated by Fred Fish - commonly known as the Fish disks - which
            were initially distributed by hand at Amiga user group meetings, but which were picked up and distributed by
            the mail order PD libraries too.
        </p>
        <p>
            PD stands for "public domain", but strictly speaking this was not accurate. In legal terms, "public domain"
            refers to a work that is no longer covered by copyright (or the copyright holder has rescinded the
            copyright and dedicated the work to the public domain). However most of the material in PD libraries was
            copyrighted but intended to be freely-distributed. Some works were <em>Shareware</em>, where you were
            allowed to use the software but if you used beyond an initial period, you were expected to send money to the
            author. There were also some variations such as beerware or postcardware! You could also find demo versions
            of commercial software in PD library collections, too.
        </p>

        <h2>About this project</h2>
