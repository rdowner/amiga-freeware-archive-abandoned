            17 Bit Software library. However these collections are not the easiest to use - sometimes the CD-ROM images
            can be read on a modern computer, but sometimes they expect to be run on an actual Amiga. Sometimes it
            requires knowledge of the Amiga operating system to extract and use images, which you may have forgotten
            about in the 20 years since you last used an Amiga! And it can be a significant challenge to move software
            between your current PC, an emulated Amiga, and a real Amiga.
